
variable1 = 10
variable2 = 5
#ADDITION:
result = variable1 + variable2
print(f"Addition of {variable1} and {variable2} is {result}")

#SUBTRACTION:
result = variable1 - variable2
print(f"Subtraction of {variable1} and {variable2} is {result}")

#MULTIPLICATION:
result = variable1 * variable2
print(f"Multiplication of {variable1} and {variable2} is {result}")
